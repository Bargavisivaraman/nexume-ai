/**
 * Majors taxonomy — 50+ academic majors with curated role lists.
 *
 * Used by the Jobs tab "Pick a major" flow:
 *   1. User picks a major
 *   2. Roles strip appears with the major's curated role list
 *   3. Click a role → sends keyword=<role.label> + industry=<major.dbIndustry>
 *      to the existing /jobs/ endpoint
 *
 * Data shape:
 *   {
 *     id:          "computer-science"   — kebab-case slug
 *     label:       "Computer Science"   — UI display
 *     emoji:       "💻"
 *     category:    "stem"               — top-level grouping
 *     dbIndustry:  "Technology"         — maps to backend INDUSTRY_MAP keys
 *     isTop:       true                 — flagged as a "top major" (100+ roles)
 *     description: "..."                — short hint shown in picker
 *     roles:       [{id, label, keywords}]
 *   }
 *
 * roles are built with the `r()` helper from arrays of strings to keep the
 * file readable. The id is derived from the label automatically.
 */

const slug = (s) =>
  s.toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");

// helper: turn ["Software Engineer", "Frontend Developer", ...] into role objects
const r = (labels) =>
  labels.map((label) => ({
    id: slug(label),
    label,
    keywords: [label.toLowerCase()],
  }));

